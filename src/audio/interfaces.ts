import { BaseResponse } from "../../types";

export interface SpeechToTextParams {
  url?: string;
  file_store_key?: string;
  language?: string;
  translate?: boolean;
  by_speaker?: boolean;
  webhook_url?: string;
  batch_size?: number;
  chunk_duration?: number;
}

export interface SpeechToTextResponse extends BaseResponse {
  text: string;
  chunks: Array<{
    timestamp: number[];
    text: string;
  }>;
  status?: "processing" | "error";
  id?: string;
  speakers?: {
    //  available when by_speaker is true
    speaker: string;
    timestamp: number[];
    text: string;
  }[];
}

export interface SpeechToTextWebhookResponse extends BaseResponse {
  status: "processing" | "error";
  id: string;
}

export interface TextToSpeechParams {
  text: string;
  accent?: SupportedAccents; // see https://docs.jigsawstack.com/additional-resources/speaker-voices for the list of supported accents.
  voice_clone_id?: string;
  return_type?: "url" | "binary" | "base64";
}

export interface TTSCloneParams {
  url?: string;
  file_store_key?: string;
  name: string;
}

export interface ListTTSVoiceClonesParams {
  limit?: number;
  page?: number;
}

export interface TextToSpeechResponse {
  status: "processing" | "error";
  id: string;
}

